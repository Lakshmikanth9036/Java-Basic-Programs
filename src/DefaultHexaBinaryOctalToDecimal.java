
public class DefaultHexaBinaryOctalToDecimal {

	public static void main(String[] args) {
		System.out.println(0456);//octal to binary equivalent value
		System.out.println(0xAB4);//hexa to binary equivalent value
		System.out.println(0b010101);//binary to binary equivalent value
		System.out.println(67);//decimal
	}

}
